// Operadores de comparacion (RELACIONALES)

let a = 10;
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

// Operadores de comparacion (IGUALDAD)
console.log(a == 10); 
console.log(a != 10);

// Utilizar estas dos
console.log(a == '10');
// Distintos tipos de datos o de valor
console.log(a !== '10');